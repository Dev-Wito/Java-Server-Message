    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        JSONObject vector = new JSONObject();
        char Pass[] = txt_pass.getPassword();
        String HumanPass = "";
        if (Pass.length != 0 && txt_login.getText().length() != 0 && txt_documento.getText().length() != 0 && txt_nombre.getText().length() != 0 && txt_correo.getText().length() != 0 && txt_celular.getText().length() != 0) {
            for (int x = 0; x < Pass.length; x++) {
                HumanPass += Pass[x];
            }
            vector.put("API", "setUsuario");
            vector.put("id", usu.getId());
            vector.put("usu", txt_login.getText());
            vector.put("key", HumanPass);
            vector.put("rol", combo_rol.getSelectedItem());
            vector.put("nuid", txt_documento.getText());
            vector.put("nom", txt_nombre.getText());
            vector.put("cor", txt_correo.getText());
            vector.put("cel", txt_celular.getText());
            txt_resumen.setText("-Enviando petici√≥n");
            String rta = pasarela.call(vector);
            vector.clear();
            vector = json.decode(rta);
            txt_resumen.setText(vector.get("msg").toString());
            cargarTabla();
        } else {
            txt_resumen.setText("-Verifica que todos los campos esten llenos");
        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (usu.getId() != 0 && Globales.USER_ID != usu.getId()) {
            JSONObject vector = new JSONObject();
            vector.put("API", "dropUser");
            vector.put("id", usu.getId());
            String rta = pasarela.call(vector);
            vector.clear();
            vector = json.decode(rta);
            txt_resumen.setText(vector.get("msg").toString());
            cargarTabla();
        } else if (Globales.USER_ID == usu.getId()) {
            cargarTabla();
            txt_resumen.setText("-No te puedes eliminar a ti mismo");
            tabla_usuarios.requestFocus();
        } else {
            cargarTabla();
            txt_resumen.setText("-Selecciona un usuario");
            tabla_usuarios.requestFocus();
        }
    }//GEN-LAST:event_jButton3ActionPerformed


