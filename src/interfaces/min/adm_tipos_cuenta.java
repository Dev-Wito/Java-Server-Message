    public void limpiarTabla() {
        tabla_tipos_cuenta.setAutoCreateRowSorter(true);
        tabla_tipos_cuenta.setModel(new javax.swing.table.DefaultTableModel(
                new Object[][]{
                    {null, "Cargando..."}
                },
                new String[]{
                    "Datos", "Tipo Cuenta"
                }
        ) {
            Class[] types = new Class[]{
                java.lang.Integer.class, java.lang.String.class
            };
            boolean[] canEdit = new boolean[]{
                false, false
            };

            public Class getColumnClass(int columnIndex) {
                return types[columnIndex];
            }

            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit[columnIndex];
            }
        });
        tabla_tipos_cuenta.setSelectionMode(javax.swing.ListSelectionModel.SINGLE_SELECTION);

        jScrollPane1.setViewportView(tabla_tipos_cuenta);
        if (tabla_tipos_cuenta.getColumnModel().getColumnCount() > 0) {
            tabla_tipos_cuenta.getColumnModel().getColumn(0).setMinWidth(1);
            tabla_tipos_cuenta.getColumnModel().getColumn(0).setPreferredWidth(1);
            tabla_tipos_cuenta.getColumnModel().getColumn(0).setMaxWidth(1);
        }
    }

    public void cargarTabla() {
        limpiarTabla();
        txt_val_rem.setText("");
        DefaultTableModel MTabla = (DefaultTableModel) tabla_tipos_cuenta.getModel();
        HLoader LOAD_TB = new HLoader(MTabla, "");
        LOAD_TB.tabla4();
    }

