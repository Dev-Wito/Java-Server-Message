    private void jToggleButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton2ActionPerformed
        int UID = tabla_tipos_cuenta.getSelectedRow();
        if (UID >= 0) {
            UID = Integer.parseInt(tabla_tipos_cuenta.getValueAt(UID, 0).toString());
        } else {
            UID = -1;
        }
        JSONObject vector = new JSONObject();
        vector.put("API", "setTTPCuenta");
        vector.put("id", UID);
        vector.put("tipo", txt_val_rem.getText());
        resumen.setText(pasarela.call(vector));
        cargarTabla();
    }//GEN-LAST:event_jToggleButton2ActionPerformed

